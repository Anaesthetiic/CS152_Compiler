#[derive(Debug)]
enum IRTok {
    // func
    Func,
    EndFunc,

    // declarations.
    Int,
    IntArray,

    // function calling routines.
    Call,
    Return,

    // input/output routines.
    Out,
    In,

    // mathematical operators.
    Mov,
    Add,
    Sub,
    Mult,
    Div,
    Mod,

    // comparison operators.
    LessThan,
    LessEqual,
    NotEqual,
    Equal,
    GreaterEqual,
    GreaterThan,

    // labels/branching
    //Label,
    Jump,
    BranchIf,
    BranchIfNot,

    Comma,
    LBrace,
    RBrace,
    Plus,

    EndInstr,

    Num(i32),
    Var(String),
}

#[derive(Debug)]
enum Op {
    Num(i32),
    Var(i32),
}

#[derive(Debug)]
enum VariableType {
    IntVar(i32),
    ArrayVar(i32, i32),
}

#[derive(Debug)]
enum MemWrite {
    IntVar(i32),
    ArrayWrite(i32, Op),
}

#[derive(Debug)]
enum MemRead {
    IntVar(i32),
    Number(i32),
    ArrayRead(i32, Op),
}

fn read_memory(variables: &HashMap<i32, i32>, arrays: &HashMap<i32, Vec<i32>>, read: &MemRead) -> i32 {
    match read {
    MemRead::IntVar(id) => *variables.get(&id).unwrap(),
    MemRead::Number(number) => *number,
    MemRead::ArrayRead(id, index) => {
        let array = arrays.get(&id).unwrap();
        let variable = read_integer_value(&variables, &index) as usize;
        array[variable]
    }
    }
}


#[derive(Debug)]
enum Bytecode {

    // EndFunc
    End,

    // declarations.
    Int(Op),
    IntArray(Op, Op),

    // input/output routines.
    Out(Op),
    In(Op),

    // mathematical operators.
    Mov(MemWrite, MemRead),
    Add(i32, Op, Op),
    Sub(i32, Op, Op),
    Mult(i32, Op, Op),
    Div(i32, Op, Op),
    Mod(i32, Op, Op),

    // comparison operators.
    LessThan(i32, Op, Op),
    LessEqual(i32, Op, Op),
    NotEqual(i32, Op, Op),
    Equal(i32, Op, Op),
    GreaterEqual(i32, Op, Op),
    GreaterThan(i32, Op, Op),
    Return(Op),
}


